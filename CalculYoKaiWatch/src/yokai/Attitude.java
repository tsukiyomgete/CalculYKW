package yokai;

public class Attitude {
    public String nom;
    public int hp;
    public int str;
    public int spr;
    public int def;
    public int spe;

    
    

    public Attitude(String nom, int Health, int Strength, int Spirit, int Defense, int Speed)
    {
        this.nom = nom;
        this.hp = Health;
        this.str = Strength;
        this.spr = Spirit;
        this.def = Defense;
        this.spe = Speed;
    }


}
