public class Attitude {
    String nom;
    int hp;
    int str;
    int spr;
    int def;
    int spe;

    Attitude(String nom, int Health, int Strength, int Spirit, int Defense, int Speed)
    {
        nom = this.nom;
        hp = Health;
        str = Strength;
        spr = Spirit;
        def = Defense;
        spe = Speed;
    }


}
