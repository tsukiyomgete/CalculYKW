public class Attitude {
    String nom;
    int hp;
    int str;
    int spr;
    int def;
    int spe;

    Attitude(String nom, int Health, int Strength, int Spirit, int Defense, int Speed)
    {
        this.nom = nom;
        this.hp = Health;
        this.str = Strength;
        this.spr = Spirit;
        this.def = Defense;
        this.spe = Speed;
    }


}
